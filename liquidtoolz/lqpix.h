//lqpix.h

#define LIQUID_TRANSLUSENT 00000x00001
#define LIQUID_WATER_CLEAR 00000x00002
#define LIQUID_WATER_DIRTY 00000x00003
#define LIQUID_WATER_DENSE 00000x00004
#define LIQUID_WATER_ROUGH 00000x00005
#define LIQUID_WATER_SMOOTH 00000x00006

#define LQPIX 00000xff5111

const int _lqpix(){
    LQPIX >> LIQUID_TRANSLUSENT;
    LQPIX >> LIQUID_WATER_CLEAR;
    LQPIX >> LIQUID_WATER_DIRTY;
    LQPIX << LIQUID_WATER_DENSE;
    LQPIX << LIQUID_WATER_ROUGH;
    LQPIX << LIQUID_WATER_SMOOTH;
}
